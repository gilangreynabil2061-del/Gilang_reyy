				Borneo FC adalah klub sepak bola profesional yang berasal dari Samarinda, Kalimantan Timur, Indonesia. Klub ini didirikan pada 7 Maret 2014 setelah pengambilalihan lisensi klub Divisi Utama Perseba Super Bangkalan oleh manajemen baru yang kemudian memindahkan markas ke Samarinda. Dikenal juga dengan julukan "Pesut Etam", Borneo FC dengan cepat berkembang menjadi salah satu kekuatan baru dalam sepak bola Indonesia. Klub ini memiliki visi membangun tim yang kompetitif dengan mengedepankan pemain muda berbakat, serta memperkuat identitas sepak bola di Pulau Kalimantan. Bermarkas di Stadion Segiri, Borneo FC bermain di kompetisi Liga 1 Indonesia dan terus menunjukkan perkembangan dari tahun ke tahun. Dengan semangat kedaerahan yang kuat, klub ini menjadi simbol kebanggaan masyarakat Kalimantan Timur dan berupaya memperluas pengaruhnya di kancah sepak bola nasional.<div id="more"><a href="article3.php">Baca selengkapnya</a></div>
				
